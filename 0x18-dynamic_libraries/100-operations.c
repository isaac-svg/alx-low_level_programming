/**
  * add - adds to numbers
  * @a: first number
  * @b: second number
  * Return: a + b
  */
int add(int a, int b)
{
	return (a + b);
}


/**
  * mul - multiplies two numbers
  * @a: first number
  * @b: second number
  * Return: a * b
  */
int mul(int a, int b)
{
	return (a * b);
}


/**
  * div - divides two numbers
  * @a: first number
  * @b: second number
  * Return: a / b
  */
int div(int a, int b)
{
	return (a / b);
}


/**
  * sub - subtructs two numbers
  * @a: first number
  * @b: second number
  * Return: a + b
  */
int sub(int a, int b)
{
	return (a - b);
}


/**
  * mod - takes the mode of two numbers
  * @a: first number
  * @b: second number
  * Return: a % b
  */
int mod(int a, int b)
{
	return (a % b);
}
